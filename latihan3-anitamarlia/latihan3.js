function lookingFor(){
    var input, edit, table, row, colomn, cell, i, j;
    input = document.getElementById("cari");
    edit = input.value.toUpperCase();
    table = document.getElementById("tabel");
    row = table.getElementsByTagName("tr");
    for(i = 1; i < row.length; i++){
        row[i].style.display = "none";

        colomn = row[i].getElementsByTagName("td");
        for(var j = 0; j < colomn.length; j++){
            cell = row[i].getElementsByTagName("td")[j];
            if(cell){
                if(cell.innerHTML.toUpperCase().indexOf(edit) > -1){
                    row[i].style.display = "";
                    break;
                }
            }
        }
    }
}